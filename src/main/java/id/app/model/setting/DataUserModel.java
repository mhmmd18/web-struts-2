package id.app.model.setting;

public class DataUserModel {
	
	private String nameUser, fullName, roleUser, isActive, tglActive, tglDeactive, tglExpire;

	public String getNameUser() {
		return nameUser;
	}

	public void setNameUser(String nameUser) {
		this.nameUser = nameUser;
	}

	public String getFullName() {
		return fullName;
	}

	public void setFullName(String fullName) {
		this.fullName = fullName;
	}

	public String getRoleUser() {
		return roleUser;
	}

	public void setRoleUser(String roleUser) {
		this.roleUser = roleUser;
	}

	public String getIsActive() {
		return isActive;
	}

	public void setIsActive(String isActive) {
		this.isActive = isActive;
	}

	public String getTglActive() {
		return tglActive;
	}

	public void setTglActive(String tglActive) {
		this.tglActive = tglActive;
	}

	public String getTglDeactive() {
		return tglDeactive;
	}

	public void setTglDeactive(String tglDeactive) {
		this.tglDeactive = tglDeactive;
	}

	public String getTglExpire() {
		return tglExpire;
	}

	public void setTglExpire(String tglExpire) {
		this.tglExpire = tglExpire;
	}

}
